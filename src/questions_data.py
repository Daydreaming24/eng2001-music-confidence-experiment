from dataclasses import dataclass, field
from typing import Dict, Optional, List


@dataclass
class Question:
    id: int  # 题号（1–10）
    name: str  # 题目名称（含“_正式”/“_干扰”）
    block: str  # "formal" 正式题 / "filler" 干扰题
    is_objective: bool  # 是否客观题（用于正确率 & 是否跳过追问）
    correct_option: Optional[str]  # 客观题正确选项；非客观题为 None
    text: str  # 题干文本（包括场景描述和问题）
    options: Dict[str, str]  # 选项内容 {"A": "...", ...}
    followups: Dict[str, str]  # 追问内容 {"A": "...", ...}；无追问则 {}
    table: Optional[Dict[str, Dict[str, str]]] = None  # 特殊表格信息（用于防晒霜题）

    # 使旧版代码属性兼容
    @property
    def objective(self) -> bool:
        return self.is_objective

    @property
    def is_formal(self) -> bool:
        return self.block == "formal"

    @property
    def followup(self) -> Dict[str, str]:
        return self.followups


